import math as m

distance = m.sqrt(m.pow(3 - 1, 2) + m.pow(4 - 1, 2)) ;

print(distance);

distance = m.sqrt(m.pow(10 - 0, 2) + m.pow(10 - 0, 2));

print(distance);

distance = m.sqrt(m.pow(20 - 2, 2) + m.pow(30 - 7, 2));

print(distance);

